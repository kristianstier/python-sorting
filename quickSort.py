def quickSort(A):
    if len(A) > 1:
        q = partition(A)
        A1 = quickSort(A[:q])
        A2 = quickSort(A[q:])
        A = A1 + A2 
    return A 

def partition(A):
    x = A[len(A)-1]
    i = 0
    j = 0
    while j < len(A)-1:
        if A[j] <= x:
            z = A[j]
            A[j] = A[i]
            A[i] = z
            i = i + 1
        j = j + 1    
    A[len(A)-1] = A[i]
    A[i] = x
    return i     

